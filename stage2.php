	<p/>Le stage de 2ème année s'est effectué une nouvelle fois au sein du Conseil Général du Cantal : http://www.cantal.fr/

<p/>Ce stage s'est déroulé du 27 janvier au 7 mars 2014.

 

<p align=center><b>Voici le cahier des charges du 1er projet </b>

<p/>Périmètre du projet : 

<p/>Le projet informatique concerné par le stage est VESTA, c'est un outil informatique commun gérant les métiers de l'ASE (Aide Social à l'Enfance) et du SIPEIJ (Service des informations Préoccupantes). Ce projet est déjà en production et intègre les fonctionnalités suivantes :

<p/>- Gestion du dossier famille

<p/>- Procédure Information Préoccupante,

<p/>- Procédure Assistance à Domicile,

<p/>- Procédure Aide Financière,

<p/>- Procédure Travailleuse Familiale,

<p/>- Procédure Placement,

<p/>- Module de paiement des aides, accessoires et de la paye des assistantes familiales.

<p align="center"><b>Les attentes du projets :</b><p/>

<p/>Il est demandé dans le cadre du stage la réalisation d'éditions correspondant à des courriers envoyés aux familles, Procureurs, Juge des Enfants, organismes divers, etc.

<p/>Chaque courrier devra également comporter divers exemplaires.

<p/>L’automatisation de ces courriers a plusieurs objectifs :

<p/>- Gain de temps pour l'édition des courriers

<p/>- Harmonisation (mise en forme, signature automatique, orthographe, ...)

<p align="center"><b>Environnement technique :</b><p/>

<p/>Les outils et technologies utilisés pour le développement, avec le soutien du SEDI, sont :

<p/>- AGL : Visual Studio 2013

<p/>- Générateur d'éditions Crystal Reports ou Microsoft Reports

<p/>- Base de données : Microsoft SQL Server 2008

<p/>- Langages : VB.NET ou C# , java Script , SQL

<p/>- ISS 7, ASP.NET, FrameWork 4.5,

<p/>- Active Directory

