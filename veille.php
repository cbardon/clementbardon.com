En français , montre intelligente . C'est un dispositif informatique portable porté sur le poignet d' un utilisateur, et qui possède des fonctionnalités et des capacités semblables à celles d'un téléphone . Les Smartwatchs sont conçues pour fonctionner seuls ou quand elles sont associées avec un smartphone , elles fournissent des fonctionnalités comme la connexion à Internet , l'exécution des applications mobiles , les appels ,  la messagerie par texte ou vidéo , elles donnent les coordonnées GPS et directions de localisation , et plus encore.
<p/>
De nombreuses sociétés ont investi le marché des SmartWatchs. Les principales sont : Samsung ( Galaxy Gear), Sony (Sony SmartWatch), Qualcomm(Toq SmartWatch) ,GooPhone Smart Watch ,Google (La Nexus). Apple va bientôt sortir sa SmartWatch , elle s'appellera la IWatch.
<p/>
<p>D'un point de vu professionnel , les SmartWatchs peuvent ainsi être comparées aux bippers</p>
	<p align='center'><img src="images/bipper.jpg" alt=""  width=300px; /></p>
	<p>De plus en plus de société donnent à leurs employés des téléphones portables professionnels. Ainsi grâce à la connexion entre SmartPhone et SmartWatch , les employés peuvent recevoir des informations directement sur leurs montres. Par exemple, un technicien réseau qui se déplace en permanence sur différents lieux, peut savoir en un mouvement de doigts, où il est attendu ou bien la prochaine mission qui l'attend.</p>
	<p/>
	<p>Il y a également, pour un chef d'entreprise, un moyen de géolocaliser ses employés, il peut ainsi savoir où ils sont grâce au système de coordonnées GPS.</p>
	<p/>
	<p>Il existe aussi des SmartWatchs qui n'ont pas besoin d'être connectées à SmartPhone pour fonctionner. Ces SmartWatchs ont leur propre connection à Internet , elles possèdent donc quasiment les mêmes caractéristiques que celles qui sont connectées.</p>
	<p></p>
	<p>Mais attention , ces SmartWatchs ne sont compatibles pour le moment qu'avec peu de SmartPhone , seuls les plus récents, comme le Galaxy S5 ou le Galaxy note 3 pour les SmartWatchs Samsung , et les derniers Xperia pour les SmartWatchs Sony.</p>
	<p> Chaque SmartWatch dépend d'un nombre très limité de SmartPhone. Pour résumer, la compatibilié entre SmartWatch et SmartPhone se définit par la marque des deux produits. </p>
