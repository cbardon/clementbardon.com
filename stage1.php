	<p>Ma mission pour ce stage de 1ère année a été de développer en C# une application de transfert de fichier appelé Lilou.

<p/><b>Etapes</b>

<br>- Logiciel utilisé : Visual Studio 2012.

<p/>- Après avoir étudié le cahier des charges et demander au maitre de stage des précisions sur certains points, la 1ère partie a été de réaliser l'application en client lourd, sans base de données donc tout en dur.



<p/>- Cette étape passée, mon maitre de stage m'a donné pour but de coder cette application de transfert de ficher en C# Web.

<p/>- Tout comme la version client lourd , le développement de l'application s'est déroulé sur Microsoft Visual Studio 2012 , mais l'application necessitant une Base de Données , cela se passa sur Microsoft SQL Server Management Studio.

<p/>- Mais avant des créer cette base de données , il fallait produire un MCD, une fois que mon tuteur fut en accord avec celle-ci, l'étape de la création et du remplissage de la BDD a enfin commencé.

<p/- >L'objectif de cette base de données était de rendre l'application plus dynamique , le code peut être modifié rapidement sans avoir à modifier toutes les pages de codes.

